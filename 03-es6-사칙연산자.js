//숫자를 저장할 변수 3개
let num1 = 10;
let num2 = 3, num3 = 5; //콤마로 여러개 동시 선언

//결과를 저장할 변수 만들기
let result;




//1. num1 + num2 ==> result
result = num1 + num2;
console.log(result); //13


//2. num1 - num3 ==> result
result = num1 - num3;
console.log(result); //5


//3. num1 * num3 ==> result
result = num1 * num2;
console.log(result); //30


//4. num1 / num2 ==> result
result = num1 / num2;
console.log(result); //3.3333...


//5. 나머지 연산자  (홀짝구분할때)
result = num1 % num2;
console.log(result); //1